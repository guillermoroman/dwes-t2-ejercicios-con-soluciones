function ordenarArray(&$array) {
    $n = count($array);
    for ($i = 0; $i < $n - 1; $i++) {
        for ($j = 0; $j < $n - $i - 1; $j++) {
            if ($array[$j] > $array[$j + 1]) {
                // Intercambiar los elementos
                $temp = $array[$j];
                $array[$j] = $array[$j + 1];
                $array[$j + 1] = $temp;
            }
        }
    }
}

// Ejemplo de uso:
$array = [5, 1, 4, 2, 8];
ordenarArray($array);
print_r($array); // Salida: Array ( [0] => 1 [1] => 2 [2] => 4 [3] => 5 [4] => 8 )
